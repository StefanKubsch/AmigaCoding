;
; ULONG lwmf_Random(void);
;

_lwmf_Random::
	move.l  seed(pc),d0
	mulu.l  #$41C64E6D,d0
	add.l   #$3039,d0
	move.l  d0,seed
	rts

; ***************************************************************************************************
; * Variables                                                                                       *
; ***************************************************************************************************

seed:
	dc.l    $12345678